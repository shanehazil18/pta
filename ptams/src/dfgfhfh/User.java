
package dfgfhfh;

public class User {
    private String a;
    private String sy;
    private String id;
  

    
    public User(String A,String Sy,String Id)
    {
        this.a = A;
        this.sy = Sy;
        this.id = Id;


    }

    
    public String getA()
    {
        return a;
    }
    
    public String getSy()
    {
        return sy;
    }
    public String getId()
    {
        return id;
    }


   
}
