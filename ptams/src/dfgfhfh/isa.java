
package dfgfhfh;

public class isa {
    private String sy;
    private String id;
    private String datepaid;

    
    public isa (String Sy,String Id, String Datepaid)
    {
        this.sy = Sy;
        this.id = Id;

        this.datepaid = Datepaid;

    }

 
    
    public String getSy()
    {
        return sy;
    }
    public String getId()
    {
        return id;
    }
    public String getDatepaid()
    {
        return datepaid;
    }
    
}
